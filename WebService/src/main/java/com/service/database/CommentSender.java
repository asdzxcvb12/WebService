package com.service.database;

public class CommentSender {
	private String inx;
	private String idx;
	private String board_name;
	private String id;
	private String nickname;
	private String con_comment;
	
	
	public String getInx() {
		return inx;
	}
	public void setInx(String inx) {
		this.inx = inx;
	}
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getBoard_name() {
		return board_name;
	}
	public void setBoard_name(String board_name) {
		this.board_name = board_name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getCon_comment() {
		return con_comment;
	}
	public void setCon_comment(String con_comment) {
		this.con_comment = con_comment;
	}
	
	
}
